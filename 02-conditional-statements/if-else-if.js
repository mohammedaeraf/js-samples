let marks = 77;

let grade;
let result;

if (marks >= 90){
  grade = "A";
} else if (marks >= 70) {
  grade = "B";
} else if (marks >= 50) {
  grade = "C";
} else if (marks >= 40) {
  grade = "D";
} else {
  grade = "E";
}



console.log(`${result}. Your grade - ${grade}`);



